    private void montaTabela() {
        ArrayList<Usuario> usuarios = controlaUsuario.recuperarTodos();
        if (usuarios == null) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar usuarios");
        } else {
            tblUsuarios.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "ID";
                        case 1:
                            return "Usuario";
                        case 2:
                            return "Senha";

                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 3;
                }

                @Override
                public int getRowCount() {
                    return usuarios.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Usuario u = usuarios.get(rowIndex);

                    if (u != null) {
                        switch (columnIndex) {
                            case 0:
                                return u.getId();
                            case 1:
                                return u.getUsuario();
                            case 2:
                                return u.getSenha();

                        }

                    }

                    return "n/d";
                }
            });

            tblUsuarios.getColumnModel().getColumn(0).setMinWidth(80);
            tblUsuarios.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblUsuarios.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

